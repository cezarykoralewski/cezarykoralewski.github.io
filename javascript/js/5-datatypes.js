'use strict';

// typ liczbowy
var liczba = 10;
console.log(liczba);

var liczbaZmiennoprzecinkowa = 3.14;
console.log(liczbaZmiennoprzecinkowa);

//typ łańcuchowy - string

var name = 'Cezary';
console.log(name);

var wzrost = 180 + 'cm';
console.log(wzrost);

// typ logiczny - boolean

var prawda = true;
console.log(prawda);

var sprawdzam = (3 > 1);
console.log(sprawdzam);

//typy specjalne

var niezdefiniowana;
console.log(niezdefiniowana);

var nic = null;
console.log(nic);

console.log(12 * null);
console.log(null * 10);
console.log('test' * null);

// połączenie typow

console.log('4' + 3);
console.log('4' - 3);
console.log('4' * 3);
console.log('4' / 0);
